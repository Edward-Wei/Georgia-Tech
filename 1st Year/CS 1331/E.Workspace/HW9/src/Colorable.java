import java.awt.Color;

/**
 * An Object implementing the Colorable
 * interface has a Color that can be
 * obtained.
 *
 * @author Aaron Clarke
 * @version 03/27/2011
 */
public interface Colorable
{
	/**
	 * A getter for the Color of the
	 * Colorable object.
	 *
	 * @return The Color of this Colorable
	 *				object.
	 */
	public Color getColor();
}